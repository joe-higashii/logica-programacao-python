"""
Exercício 1: Tipagem Dinâmica

Objetivo: Demonstrar a mudança de tipo em variáveis
"""

# Variável inicialmente como inteiro
dado = 42
print(f"Valor: {dado} | Tipo: {type(dado)}")

# Mudança para float
dado = 3.14
print(f"Novo Valor: {dado} | Tipo: {type(dado)}")

# Mudança para string
dado = "Python"
print(f"Novo Valor: {dado} | Tipo: {type(dado)}")

# Mudança para booleano
dado = True
print(f"Novo Valor: {dado} | Tipo: {type(dado)}")
